    @Override
    protected void init() {
        OverlapProcessor overlapProcessor = new OverlapProcessorDefaultImpl();

        MoveBlockerChecker moveBlockerChecker = new MoveBlockerCheckerDefaultImpl();
        moveBlockerChecker.setMoveBlockerRules(new PacmanMoveBlockers());

        GameOverlapRules overlapRules = new GameOverlapRules(life[0], score[0], endOfGame);
        overlapProcessor.setOverlapRules(overlapRules);

        UniqueGameUniverse.init(moveBlockerChecker, overlapProcessor);

        universe = UniqueGameUniverse.getInstance();

        AgeAbstractFactory age = new AgeMiddleFactory();


        UnitFactory factoryTeam1 = new PlayableUnitFactory(universe, age);
        factoryTeam1.setupCanvas(canvas);

        UnitFactory factoryTeam2 = new AIUnitFactory(universe, age);
        factoryTeam2.setupCanvas(canvas);

        Team t1 = new Team("Team 1", factoryTeam1);
        Team t2 = new Team("Team 2", factoryTeam2);

        gameBoard = new GameUniverseViewPortDefaultImpl(canvas, universe);
        ((CanvasDefaultImpl) canvas).setDrawingGameBoard(gameBoard);

        // Filling up the universe with basic non movable entities and inclusion in the universe
        for (int i = 0; i < tab.length; ++i) {
            for (int j = 0; j < tab[i].length; ++j) {
                if (tab[i][j] == 1) {
                    universe.addGameEntity(new Ground(canvas, j * Main.SPRITE_SIZE, i * Main.SPRITE_SIZE));
                }
            }
        }

        factoryTeam1.addSoldier(14, 17);
        factoryTeam1.addBarrack(20, 22);
        factoryTeam1.addGarage(20, 20);
        factoryTeam1.addSoldier(14, 16);
        factoryTeam1.addVehicle(15, 16);

        factoryTeam2.addSoldier(2, 2);
        factoryTeam2.addSoldier(6, 6);
        factoryTeam2.addSoldier(6, 7);
        factoryTeam2.addBarrack(2, 3);
        factoryTeam2.addVehicle(2, 4);
        factoryTeam2.addGarage(2, 5);


        /**** Round Test ****/

        UnitVisitor visitor = new AddSimpleUnitObserver(new DeadUnitCounterObserver());
        t1.accept(visitor);
        t2.accept(visitor);

        WeaponCounterVisitor c1 = new WeaponCounterVisitor();
        t1.accept(c1);
        System.out.println(t1.getName() + " has got " + c1.attWeapon + " attack weapons and " + c1.defWeapon + " defense weapon");
        c1.clear();
        t2.accept(c1);
        System.out.println(t2.getName() + " has got " + c1.attWeapon + " attack weapons and " + c1.defWeapon + " defense weapon");

        int round = 0;
        while (t1.alive() && t2.alive()) {
            System.out.println("Round  #" + round++);
            float st1 = t1.strike();
            System.out.println(t1.getName() + " attack with force : " + st1);
            t2.parry(st1);
            float st2 = t2.strike();
            System.out.println(t2.getName() + " attack with force : " + st2);
            t1.parry(st2);
        }
        System.out.println("The end ... " + (t1.alive() ? t1.getName() : t2.getName()) + " won.");
    }

